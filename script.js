function openGame(game) {

    if (game === "snake") {
        window.location.href = "games/snake.html";
    }

    if (game === "tictactoe") {
        window.location.href = "games/tictactoe.html";
    }

    if (game === "cowsbulls") {
        window.location.href = "games/cowsbulls.html";
    }
}